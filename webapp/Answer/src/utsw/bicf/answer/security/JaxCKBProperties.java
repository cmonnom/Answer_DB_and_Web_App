package utsw.bicf.answer.security;

public class JaxCKBProperties {

	String geneServlet;
	String geneUrl;
	String genePaidUrl;
	String variantServlet;
	String variantUrl;
	String variantPaidUrl;
	
	public JaxCKBProperties() {
		super();
	}

	public String getGeneUrl() {
		return geneUrl;
	}

	public void setGeneUrl(String geneUrl) {
		this.geneUrl = geneUrl;
	}

	public String getGeneServlet() {
		return geneServlet;
	}

	public void setGeneServlet(String geneServlet) {
		this.geneServlet = geneServlet;
	}

	public String getVariantServlet() {
		return variantServlet;
	}

	public void setVariantServlet(String variantServlet) {
		this.variantServlet = variantServlet;
	}

	public String getVariantUrl() {
		return variantUrl;
	}

	public void setVariantUrl(String variantUrl) {
		this.variantUrl = variantUrl;
	}

	public String getGenePaidUrl() {
		return genePaidUrl;
	}

	public void setGenePaidUrl(String genePaidUrl) {
		this.genePaidUrl = genePaidUrl;
	}

	public String getVariantPaidUrl() {
		return variantPaidUrl;
	}

	public void setVariantPaidUrl(String variantPaidUrl) {
		this.variantPaidUrl = variantPaidUrl;
	}




	
	
}
