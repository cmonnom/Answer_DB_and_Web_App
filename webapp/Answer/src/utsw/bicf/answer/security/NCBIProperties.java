package utsw.bicf.answer.security;

public class NCBIProperties {

	String url;
	String epost;
	String esummary;
	String queryKey;
	String webEnv;
	String apiKey;
	String esearchGene;
	String esummaryGene;
	String ncbiGeneUrl;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public NCBIProperties() {
	}

	public String getEpost() {
		return epost;
	}

	public void setEpost(String epost) {
		this.epost = epost;
	}

	public String getEsummary() {
		return esummary;
	}

	public void setEsummary(String esummary) {
		this.esummary = esummary;
	}

	public String getQueryKey() {
		return queryKey;
	}

	public void setQueryKey(String queryKey) {
		this.queryKey = queryKey;
	}

	public String getWebEnv() {
		return webEnv;
	}

	public void setWebEnv(String webEnv) {
		this.webEnv = webEnv;
	}

	public String getApiKey() {
		return apiKey;
	}

	public void setApiKey(String apiKey) {
		this.apiKey = apiKey;
	}

	public String getEsearchGene() {
		return esearchGene;
	}

	public void setEsearchGene(String esearchGene) {
		this.esearchGene = esearchGene;
	}

	public String getEsummaryGene() {
		return esummaryGene;
	}

	public void setEsummaryGene(String esummaryGene) {
		this.esummaryGene = esummaryGene;
	}

	public String getNcbiGeneUrl() {
		return ncbiGeneUrl;
	}

	public void setNcbiGeneUrl(String ncbiGeneUrl) {
		this.ncbiGeneUrl = ncbiGeneUrl;
	}

	
	
	
}
