package utsw.bicf.answer.security;

public class OncoKBProperties {

	String searchUrl;
	String summaryUrl;
	String oncoKBGeneUrl;
	
	public OncoKBProperties() {
		super();
	}

	public String getSearchUrl() {
		return searchUrl;
	}

	public void setSearchUrl(String searchUrl) {
		this.searchUrl = searchUrl;
	}

	public String getSummaryUrl() {
		return summaryUrl;
	}

	public void setSummaryUrl(String summaryUrl) {
		this.summaryUrl = summaryUrl;
	}

	public String getOncoKBGeneUrl() {
		return oncoKBGeneUrl;
	}

	public void setOncoKBGeneUrl(String oncoKBGeneUrl) {
		this.oncoKBGeneUrl = oncoKBGeneUrl;
	}


	
	
}
